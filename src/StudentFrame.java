    // Helper method to load database data into the JTable
    private void refreshTable() {
        List<Student> list = dao.getAllStudents();
        String[] columnNames = {"ID", "First Name", "Last Name", "Age", "Email"};
        Object[][] data = new Object[list.size()][5];
        
        for (int i = 0; i < list.size(); i++) {
            data[i][0] = list.get(i).getStudentId();
            data[i][1] = list.get(i).getFirstName();
            data[i][2] = list.get(i).getLastName();
            data[i][3] = list.get(i).getAge();
            data[i][4] = list.get(i).getEmail();
        }
        studentTable.setModel(new javax.swing.table.DefaultTableModel(data, columnNames));
    
    }

