    private void studentTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_studentTableMouseClicked
        int selectedRow = studentTable.getSelectedRow();
        if (selectedRow != -1)
        {
            javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) studentTable.getModel();
            
            String firstName = model.getValueAt(selectedRow, 1).toString();
        String lastName = model.getValueAt(selectedRow, 2).toString();
        String age = model.getValueAt(selectedRow, 3).toString();
        String email = model.getValueAt(selectedRow, 4).toString();
        
        txtFirstName.setText(firstName);
        txtLastName.setText(lastName);
        txtAge.setText(age);
        txtEmail.setText(email);
        }
        
    }//GEN-LAST:event_studentTableMouseClicked

    
