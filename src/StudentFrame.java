    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selectedRow = studentTable.getSelectedRow();
    if (selectedRow != -1) {
        int id = (int) studentTable.getValueAt(selectedRow, 0); // Gets the Student ID
        
        int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this student?");
        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            dao.deleteStudent(id);
            refreshTable(); // Refresh the list
            javax.swing.JOptionPane.showMessageDialog(this, "Student Deleted Successfully!");
        }
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Please select a student from the table first.");
    }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
      int selectedRow = studentTable.getSelectedRow();
    if (selectedRow != -1) {
        int id = (int) studentTable.getValueAt(selectedRow, 0);
        
        // Collect new info from the text fields
        Student s = new Student();
        s.setStudentId(id);
        s.setFirstName(txtFirstName.getText());
        s.setLastName(txtLastName.getText());
        s.setAge(Integer.parseInt(txtAge.getText()));
        s.setEmail(txtEmail.getText());
        
        // This is the part you were missing!
        dao.updateStudent(s);
        refreshTable();
        javax.swing.JOptionPane.showMessageDialog(this, "Student Updated!");
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Please select a student from the table first.");
    }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
       try {
        // 1. Collect info from the text fields
        Student s = new Student();
        s.setFirstName(txtFirstName.getText());
        s.setLastName(txtLastName.getText());
        s.setAge(Integer.parseInt(txtAge.getText()));
        s.setEmail(txtEmail.getText());

        // 2. Add to database (Assuming your DAO method is called addStudent)
        dao.addStudent(s); 

        // 3. Refresh the table to show the new student
        refreshTable();

        // 4. Clear the text fields so they are empty for the next entry
        txtFirstName.setText("");
        txtLastName.setText("");
        txtAge.setText("");
        txtEmail.setText("");

        javax.swing.JOptionPane.showMessageDialog(this, "Student Added Successfully!");
        
    } catch (NumberFormatException e) {
        // This catches the error if someone types a word in the Age box instead of a number
        javax.swing.JOptionPane.showMessageDialog(this, "Please enter a valid number for Age.");
    }
    }//GEN-LAST:event_btnAddActionPerformed

